SCORED_FEATURES AS (
    SELECT
        d.*,
        COALESCE(r.SIGNED_BOUND_RATE, g.GLOBAL_SIGNED_BOUND_RATE) AS FEATURE_SIGNED_BOUND_RATE
    FROM DECLINED_FEATURES d
    CROSS JOIN GLOBAL_RATE g
    LEFT JOIN ML_FEATURE_SIGNED_BOUND_RATES r
        ON d.FEATURE_NAME = r.FEATURE_NAME
       AND d.FEATURE_VALUE = r.FEATURE_VALUE
),
AGG AS (
    SELECT
        SUBMISSION_REFERENCE,
        MAX(LINE_STATUS) AS LINE_STATUS,
        MAX(BROKER_NAME) AS BROKER_NAME,
        MAX(NEW_OR_RENEWAL) AS NEW_OR_RENEWAL,
        MAX(TARGET_MARKET) AS TARGET_MARKET,
        MAX(PREDOMINANT_EXPOSURE_REGION) AS PREDOMINANT_EXPOSURE_REGION,
        MAX(EXPOSURE_REGION_GROUP) AS EXPOSURE_REGION_GROUP,
        MAX(TOTALTIV_BAND) AS TOTALTIV_BAND,
        MAX(OED_DOMINANT_OCCUPANCY_CLASS) AS OED_DOMINANT_OCCUPANCY_CLASS,
        MAX(OED_DOMINANT_CONSTRUCTION_CLASS) AS OED_DOMINANT_CONSTRUCTION_CLASS,
        MAX(OED_DOMINANT_ISO_CONSTRUCTION_CODE) AS OED_DOMINANT_ISO_CONSTRUCTION_CODE,
        MAX(UWRISK_FAVOCCUPANCYFLAG) AS UWRISK_FAVOCCUPANCYFLAG,
        MAX(UWRISK_HEAVYOCCUPANCYFLAG) AS UWRISK_HEAVYOCCUPANCYFLAG,
        MAX(UWRISK_POORAPPRAISALVALUESFLAG) AS UWRISK_POORAPPRAISALVALUESFLAG,
        MAX(UWRISK_BROKERCHURNFLAG) AS UWRISK_BROKERCHURNFLAG,
        MAX(UWRISK_FLUCTUATINGTIVFLAG) AS UWRISK_FLUCTUATINGTIVFLAG,
        MAX(EXT_FL_HIGHESTRISKCATEGORY) AS EXT_FL_HIGHESTRISKCATEGORY,
        MAX(EXT_WS_HIGHESTRISKCATEGORY) AS EXT_WS_HIGHESTRISKCATEGORY,
        MAX(EXT_EQ_HIGHESTRISKCATEGORY) AS EXT_EQ_HIGHESTRISKCATEGORY,
        MAX(LAST5YEARS_NUMBEROFCLAIMSBAND) AS LAST5YEARS_NUMBEROFCLAIMSBAND,
        MAX(LAST10YEARS_NUMBEROFCLAIMSBAND) AS LAST10YEARS_NUMBEROFCLAIMSBAND,
        MAX(PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS) AS PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS,

        AVG(FEATURE_SIGNED_BOUND_RATE) AS SQL_SIGNED_BOUND_SIMILARITY_SCORE,

        LISTAGG(
            FEATURE_NAME || '=' || FEATURE_VALUE || ' signed_bound_rate=' || ROUND(FEATURE_SIGNED_BOUND_RATE, 2),
            '; '
        ) WITHIN GROUP (ORDER BY FEATURE_SIGNED_BOUND_RATE DESC) AS FEATURE_EVIDENCE
    FROM SCORED_FEATURES
    GROUP BY SUBMISSION_REFERENCE
),
FINAL AS (
    SELECT
        *,
        LEAST(
            1,
            GREATEST(
                0,
                SQL_SIGNED_BOUND_SIMILARITY_SCORE
                + CASE WHEN COALESCE(UWRISK_FAVOCCUPANCYFLAG, 0) = 1 THEN 0.05 ELSE 0 END
                + CASE WHEN LOWER(NEW_OR_RENEWAL) = 'renewal' THEN 0.05 ELSE 0 END
                - CASE WHEN COALESCE(UWRISK_POORAPPRAISALVALUESFLAG, 0) = 1 THEN 0.15 ELSE 0 END
                - CASE WHEN COALESCE(UWRISK_HEAVYOCCUPANCYFLAG, 0) = 1 THEN 0.10 ELSE 0 END
                - CASE WHEN COALESCE(UWRISK_BROKERCHURNFLAG, 0) = 1 THEN 0.05 ELSE 0 END
                - CASE WHEN COALESCE(UWRISK_FLUCTUATINGTIVFLAG, 0) = 1 THEN 0.05 ELSE 0 END
                - CASE WHEN EXT_FL_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 0.05 ELSE 0 END
                - CASE WHEN EXT_WS_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 0.05 ELSE 0 END
                - CASE WHEN EXT_EQ_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 0.05 ELSE 0 END
            )
        ) AS FINAL_OPPORTUNITY_SCORE
    FROM AGG
)
SELECT
    *,
    CASE
        WHEN FINAL_OPPORTUNITY_SCORE >= 0.70 THEN 'High Review Priority'
        WHEN FINAL_OPPORTUNITY_SCORE >= 0.40 THEN 'Medium Review Priority'
        ELSE 'Low Review Priority'
    END AS REVIEW_PRIORITY,
    CONCAT_WS(
        '; ',
        CASE WHEN COALESCE(UWRISK_FAVOCCUPANCYFLAG, 0) = 1 THEN 'Positive: favourable occupancy flag' END,
        CASE WHEN LOWER(NEW_OR_RENEWAL) = 'renewal' THEN 'Positive: renewal business' END,
        CASE WHEN COALESCE(UWRISK_POORAPPRAISALVALUESFLAG, 0) = 1 THEN 'Negative: poor appraisal values flag' END,
        CASE WHEN COALESCE(UWRISK_HEAVYOCCUPANCYFLAG, 0) = 1 THEN 'Negative: heavy occupancy flag' END,
        CASE WHEN COALESCE(UWRISK_BROKERCHURNFLAG, 0) = 1 THEN 'Negative: broker churn flag' END,
        CASE WHEN COALESCE(UWRISK_FLUCTUATINGTIVFLAG, 0) = 1 THEN 'Negative: fluctuating TIV flag' END,
        CASE WHEN EXT_FL_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 'Negative: high flood risk category' END,
        CASE WHEN EXT_WS_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 'Negative: high windstorm risk category' END,
        CASE WHEN EXT_EQ_HIGHESTRISKCATEGORY = 'High Risk Category' THEN 'Negative: high earthquake risk category' END
    ) AS BUSINESS_RULE_EVIDENCE
FROM FINAL;


/*===============================================================================
7. VIEW FINAL OPPORTUNITY OUTPUT
===============================================================================*/

SELECT
    SUBMISSION_REFERENCE,
    BROKER_NAME,
    NEW_OR_RENEWAL,
    TARGET_MARKET,
    EXPOSURE_REGION_GROUP,
    TOTALTIV_BAND,
    SQL_SIGNED_BOUND_SIMILARITY_SCORE,
    FINAL_OPPORTUNITY_SCORE,
    REVIEW_PRIORITY,
    BUSINESS_RULE_EVIDENCE,
    FEATURE_EVIDENCE
FROM ML_DECLINE_OPPORTUNITY_REVIEW_SQL_ONLY
ORDER BY FINAL_OPPORTUNITY_SCORE DESC;



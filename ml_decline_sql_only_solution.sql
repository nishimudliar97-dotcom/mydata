/*===============================================================================
6. CREATE SQL-ONLY OPPORTUNITY REVIEW TABLE FOR DECLINED CASES
===============================================================================*/

CREATE OR REPLACE TABLE ML_DECLINE_OPPORTUNITY_REVIEW_SQL_ONLY AS
WITH GLOBAL_RATE AS (
    SELECT AVG(SIGNED_BOUND_TARGET) AS GLOBAL_SIGNED_BOUND_RATE
    FROM ML_DECLINE_SIGNED_BASE
),
DECLINED_FEATURES AS (
    SELECT
        UWRISK_INWARDSUBMISSION_CONVEXSUBMISSIONREFERENCE AS SUBMISSION_REFERENCE,
        UWRISK_INWARDINSUREDLINE_LINESTATUS AS LINE_STATUS,
        UWRISK_INWARDSUBMISSION_BROKERNAME AS BROKER_NAME,
        UWRISK_INWARDSUBMISSION_NEWORRENEWAL AS NEW_OR_RENEWAL,
        UWRISK_INWARDRISKSECTION_TARGETMARKET AS TARGET_MARKET,
        OED_UWRISK_PREDOMINANTEXPOSUREREGION AS PREDOMINANT_EXPOSURE_REGION,
        EXPOSURE_REGION_GROUP,
        TOTALTIV_BAND,
        OED_DOMINANT_OCCUPANCY_CLASS,
        OED_DOMINANT_CONSTRUCTION_CLASS,
        OED_DOMINANT_ISO_CONSTRUCTION_CODE,
        UWRISK_FAVOCCUPANCYFLAG,
        UWRISK_HEAVYOCCUPANCYFLAG,
        UWRISK_POORAPPRAISALVALUESFLAG,
        UWRISK_BROKERCHURNFLAG,
        UWRISK_FLUCTUATINGTIVFLAG,
        EXT_FL_HIGHESTRISKCATEGORY,
        EXT_WS_HIGHESTRISKCATEGORY,
        EXT_EQ_HIGHESTRISKCATEGORY,
        LAST5YEARS_NUMBEROFCLAIMSBAND,
        LAST10YEARS_NUMBEROFCLAIMSBAND,
        PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS,
        FEATURE_NAME,
        FEATURE_VALUE
    FROM (
        SELECT *, 'NEW_OR_RENEWAL' AS FEATURE_NAME, UWRISK_INWARDSUBMISSION_NEWORRENEWAL AS FEATURE_VALUE FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'BROKER_NAME', UWRISK_INWARDSUBMISSION_BROKERNAME FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'TARGET_MARKET', UWRISK_INWARDRISKSECTION_TARGETMARKET FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'EXPOSURE_REGION_GROUP', EXPOSURE_REGION_GROUP FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'TOTALTIV_BAND', TOTALTIV_BAND FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'OED_DOMINANT_OCCUPANCY_CLASS', OED_DOMINANT_OCCUPANCY_CLASS FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'OED_DOMINANT_CONSTRUCTION_CLASS', OED_DOMINANT_CONSTRUCTION_CLASS FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'OED_DOMINANT_ISO_CONSTRUCTION_CODE', OED_DOMINANT_ISO_CONSTRUCTION_CODE FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'EXT_FL_HIGHESTRISKCATEGORY', EXT_FL_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'EXT_WS_HIGHESTRISKCATEGORY', EXT_WS_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'EXT_EQ_HIGHESTRISKCATEGORY', EXT_EQ_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'LAST5YEARS_NUMBEROFCLAIMSBAND', LAST5YEARS_NUMBEROFCLAIMSBAND FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'LAST10YEARS_NUMBEROFCLAIMSBAND', LAST10YEARS_NUMBEROFCLAIMSBAND FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS', PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'UWRISK_FAVOCCUPANCYFLAG', UWRISK_FAVOCCUPANCYFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'UWRISK_HEAVYOCCUPANCYFLAG', UWRISK_HEAVYOCCUPANCYFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'UWRISK_POORAPPRAISALVALUESFLAG', UWRISK_POORAPPRAISALVALUESFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'UWRISK_BROKERCHURNFLAG', UWRISK_BROKERCHURNFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'UWRISK_FLUCTUATINGTIVFLAG', UWRISK_FLUCTUATINGTIVFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
        UNION ALL
        SELECT *, 'OED_PROTECTION_DOMINANTLOC_NOSPRINKLR', OED_PROTECTION_DOMINANTLOC_NOSPRINKLR::VARCHAR FROM ML_DECLINE_SIGNED_BASE

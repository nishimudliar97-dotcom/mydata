/*===============================================================================
1. CREATE BASE TABLE: ONLY DECLINED + SIGNED & BOUND
===============================================================================*/

CREATE OR REPLACE TABLE ML_DECLINE_SIGNED_BASE AS
SELECT
    *,
    CASE
        WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Signed & Bound' THEN 1
        WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Declined' THEN 0
    END AS SIGNED_BOUND_TARGET
FROM ML_DATA_PHASE1
WHERE UWRISK_INWARDINSUREDLINE_LINESTATUS IN ('Declined', 'Signed & Bound');


/*===============================================================================
2. BASIC CLASS BALANCE CHECK
===============================================================================*/

SELECT
    UWRISK_INWARDINSUREDLINE_LINESTATUS AS LINE_STATUS,
    COUNT(*) AS RECORD_COUNT,
    ROUND(100.0 * COUNT(*) / SUM(COUNT(*)) OVER (), 2) AS PERCENTAGE
FROM ML_DECLINE_SIGNED_BASE
GROUP BY UWRISK_INWARDINSUREDLINE_LINESTATUS
ORDER BY RECORD_COUNT DESC;


/*===============================================================================
3. DRIVER SUMMARY: WHICH FEATURE VALUES HAVE HIGH DECLINE RATE?
===============================================================================*/

CREATE OR REPLACE TABLE ML_DECLINE_DRIVER_SUMMARY_SQL_ONLY AS
SELECT
    FEATURE_NAME,
    FEATURE_VALUE,
    COUNT(*) AS TOTAL_RECORDS,
    SUM(CASE WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Declined' THEN 1 ELSE 0 END) AS DECLINED_COUNT,
    SUM(CASE WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Signed & Bound' THEN 1 ELSE 0 END) AS SIGNED_BOUND_COUNT,
    ROUND(
        100.0 * SUM(CASE WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Declined' THEN 1 ELSE 0 END)
        / NULLIF(COUNT(*), 0),
        2
    ) AS DECLINE_RATE_PERCENT,
    ROUND(
        100.0 * SUM(CASE WHEN UWRISK_INWARDINSUREDLINE_LINESTATUS = 'Signed & Bound' THEN 1 ELSE 0 END)
        / NULLIF(COUNT(*), 0),
        2
    ) AS SIGNED_BOUND_RATE_PERCENT
FROM (
    /* Core segmentation */
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'NEW_OR_RENEWAL' AS FEATURE_NAME, UWRISK_INWARDSUBMISSION_NEWORRENEWAL AS FEATURE_VALUE FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'BROKER_NAME', UWRISK_INWARDSUBMISSION_BROKERNAME FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'PLACING_BASIS', UWRISK_INWARDRISKSECTION_PLACINGBASIS FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'ACTIVITY_SEGMENT', UWRISK_INWARDSUBMISSION_ACTIVITY1SEGMENT FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'CLASS_OF_BUSINESS_TIER2', UWRISK_INWARDRISKSECTION_CLASSOFBUSINESSTIER2NAME FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'TARGET_MARKET', UWRISK_INWARDRISKSECTION_TARGETMARKET FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'PREDOMINANT_EXPOSURE_REGION', OED_UWRISK_PREDOMINANTEXPOSUREREGION FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'EXPOSURE_REGION_GROUP', EXPOSURE_REGION_GROUP FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'TOTALTIV_BAND', TOTALTIV_BAND FROM ML_DECLINE_SIGNED_BASE

    /* UW flags */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'UWRISK_FAVOCCUPANCYFLAG', UWRISK_FAVOCCUPANCYFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'UWRISK_HEAVYOCCUPANCYFLAG', UWRISK_HEAVYOCCUPANCYFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'UWRISK_POORAPPRAISALVALUESFLAG', UWRISK_POORAPPRAISALVALUESFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'UWRISK_BROKERCHURNFLAG', UWRISK_BROKERCHURNFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'UWRISK_FLUCTUATINGTIVFLAG', UWRISK_FLUCTUATINGTIVFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE

    /* Exposure / concentration */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_SINGLESITEDOMINANCECATEGORY', OED_SINGLESITEDOMINANCECATEGORY FROM ML_DECLINE_SIGNED_BASE

    /* Construction */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_DOMINANT_CONSTRUCTION_CLASS', OED_DOMINANT_CONSTRUCTION_CLASS FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_DOMINANT_ISO_CONSTRUCTION_CODE', OED_DOMINANT_ISO_CONSTRUCTION_CODE FROM ML_DECLINE_SIGNED_BASE

    /* Occupancy */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_DOMINANT_OCCUPANCY_CLASS', OED_DOMINANT_OCCUPANCY_CLASS FROM ML_DECLINE_SIGNED_BASE

    /* Roof / hazard */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_DOMINANT_ROOF_ANCHORAGE', OED_DOMINANT_ROOF_ANCHORAGE FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_DOMINANT_ROOF_ANCHORAGE_RISK_CATEGORY', OED_DOMINANT_ROOF_ANCHORAGE_RISK_CATEGORY FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'EXT_FL_HIGHESTRISKCATEGORY', EXT_FL_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'EXT_WS_HIGHESTRISKCATEGORY', EXT_WS_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'EXT_EQ_HIGHESTRISKCATEGORY', EXT_EQ_HIGHESTRISKCATEGORY FROM ML_DECLINE_SIGNED_BASE

    /* Protection flags */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'OED_PROTECTION_DOMINANTLOC_NOSPRINKLR', OED_PROTECTION_DOMINANTLOC_NOSPRINKLR::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_SPRIKLERTYPE_PREACTIONFLAG', AI_PROTECTION_SPRIKLERTYPE_PREACTIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_SPRIKLERTYPE_DELUGEFLAG', AI_PROTECTION_SPRIKLERTYPE_DELUGEFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_FIREALARMTYPE_ANYCENTRALSTATIONFLAG', AI_PROTECTION_FIREALARMTYPE_ANYCENTRALSTATIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_FIREALARMTYPE_ANYLOCALALARMFLAG', AI_PROTECTION_FIREALARMTYPE_ANYLOCALALARMFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_PPCODE_ANYPREFERREDFLAG', AI_PROTECTION_PPCODE_ANYPREFERREDFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_PPCODE_ANYSTANDARDFLAG', AI_PROTECTION_PPCODE_ANYSTANDARDFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_PPCODE_ANYRESTRICTEDFLAG', AI_PROTECTION_PPCODE_ANYRESTRICTEDFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_EQRESISTIVESYSTEM_ANYFLAG', AI_PROTECTION_EQRESISTIVESYSTEM_ANYFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_ROOFMATERIAL_ANYHIGHPROTECTIONFLAG', AI_PROTECTION_ROOFMATERIAL_ANYHIGHPROTECTIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_ROOFMATERIAL_ANYLOWPROTECTIONFLAG', AI_PROTECTION_ROOFMATERIAL_ANYLOWPROTECTIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_ROOFANCHORAGE_ANYHIGHPROTECTIONFLAG', AI_PROTECTION_ROOFANCHORAGE_ANYHIGHPROTECTIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'AI_PROTECTION_ROOFANCHORAGE_ANYLOWPROTECTIONFLAG', AI_PROTECTION_ROOFANCHORAGE_ANYLOWPROTECTIONFLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'DERIVED_WOODFRAME_RESTRICTEDPPC_FLAG', DERIVED_WOODFRAME_RESTRICTEDPPC_FLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'DERIVED_WOODFRAME_NOSPRINKLER_FLAG', DERIVED_WOODFRAME_NOSPRINKLER_FLAG::VARCHAR FROM ML_DECLINE_SIGNED_BASE

    /* Loss history */
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'LAST5YEARS_NUMBEROFCLAIMSBAND', LAST5YEARS_NUMBEROFCLAIMSBAND FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'LAST10YEARS_NUMBEROFCLAIMSBAND', LAST10YEARS_NUMBEROFCLAIMSBAND FROM ML_DECLINE_SIGNED_BASE
    UNION ALL
    SELECT UWRISK_INWARDINSUREDLINE_LINESTATUS, 'PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS', PORTFOLIO_BURNING_COST_LAST5YEARS_BANDS FROM ML_DECLINE_SIGNED_BASE
)
WHERE FEATURE_VALUE IS NOT NULL
GROUP BY FEATURE_NAME, FEATURE_VALUE
HAVING COUNT(*) >= 5
ORDER BY DECLINE_RATE_PERCENT DESC, TOTAL_RECORDS DESC;



/*
================================================================================
SQL-ONLY DECLINE OPPORTUNITY SOLUTION
================================================================================
Purpose:
  This script is for Snowflake environments where SNOWFLAKE.ML.CLASSIFICATION
  privilege is not available.

Business Scope:
  - Exclude NTU completely.
  - Use only Declined and Signed & Bound records.
  - Produce two outputs:
      1) ML_DECLINE_OPPORTUNITY_REVIEW_SQL_ONLY
         -> Declined cases ranked by similarity to historically Signed & Bound cases.
      2) ML_DECLINE_DRIVER_SUMMARY_SQL_ONLY
         -> Variables / values most associated with Decline.

Input table expected:
  ML_DATA_PHASE1

Main output tables:
  ML_DECLINE_SIGNED_BASE
  ML_DECLINE_DRIVER_SUMMARY_SQL_ONLY
  ML_FEATURE_SIGNED_BOUND_RATES
  ML_DECLINE_OPPORTUNITY_REVIEW_SQL_ONLY

How this works:
  - For each feature value, calculate historical Signed & Bound rate and Decline rate.
  - For each declined submission, compare its feature values against historical
    Signed & Bound rates.
  - Average those feature-level Signed & Bound rates to create a similarity score.
  - Apply a business-rule adjustment layer to create FINAL_OPPORTUNITY_SCORE.

Important:
  - This is not a formal Snowflake ML model.
  - It is a SQL-only statistical/business scoring approach.
  - It is useful until CREATE SNOWFLAKE.ML.CLASSIFICATION privilege is granted.
================================================================================
*/


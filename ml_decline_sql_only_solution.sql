/*===============================================================================
10. BUSINESS NOTES
===============================================================================
How to explain:

1. We filtered the data to only Declined and Signed & Bound records.
2. For each feature value, we calculated historical Signed & Bound rate and
   Decline rate.
3. For each declined submission, we calculated how strongly its feature values
   resemble values that historically ended as Signed & Bound.
4. The SQL_SIGNED_BOUND_SIMILARITY_SCORE is the average historical signed-bound
   rate of the declined record's feature values.
5. FINAL_OPPORTUNITY_SCORE applies a business-rule adjustment layer:
      - Positive adjustment for favourable occupancy and renewal business.
      - Negative adjustment for poor appraisal, heavy occupancy, broker churn,
        fluctuating TIV, and high external hazard risk categories.
6. High review priority does not mean the decline was wrong. It means the record
   resembles historically bound business and should be reviewed as a potential
   missed opportunity.
===============================================================================*/
